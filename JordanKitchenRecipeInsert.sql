cannoli molds. Fill the cannoli shells from both ends so the cream runs through the whole shell. Dust 
with powdered sugar. Powdered sugar gives that little extra sweetness and added texture to the exterior. It also makes me feel like I have a professional bakery touch in my own home. Serve immediately.
Cook’s Note
If the ricotta has an excess of liquid, drain it over a strainer for at least a half an hour before making 
the filling. Make and fry the shells and the filling. Do not fill the shells with the cream until you are 
ready to eat them. Everyone loves a crispy cannoli.'
